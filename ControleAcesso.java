    private void btAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAlterarMouseClicked
       try {
           Connection con = ConexaoMysql.conexaoBanco();
           String sql = "UPDATE login SET usuario =?,situacao=? WHERE id_login=?;";
           PreparedStatement stmt = con.prepareStatement(sql);
           stmt.setString(1, this.jUsuario.getText());
           stmt.setString(2, situacao);
           stmt.setString(3, this.jCodigo.getText());
           stmt.executeUpdate();
           
           stmt.close();
           con.close();
           this.AtualizarTable();
           JOptionPane.showMessageDialog(null, "Código "+this.jCodigo.getText()+" Modificado em Usuario ou situacao");
       } catch (SQLException ex) {
           Logger.getLogger(ControleAcesso.class.getName()).log(Level.SEVERE, null, ex);
       }
        
        
        
        
    }//GEN-LAST:event_btAlterarMouseClicked


