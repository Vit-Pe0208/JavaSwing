    private void btCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btCadastrarMouseClicked
        try {
            Connection coin =ConexaoMysql.conexaoBanco();
            String sql = "INSERT INTO login(usuario,senha,tipo_usuario)"
                    + "VALUES(?,UPPER(MD5('Mudar123')),?);";
            PreparedStatement stmt = coin.prepareStatement(sql);
            stmt.setString(1,this.jUsuario.getText());
            stmt.setString(2,this.jTipoUsuario.getSelectedItem().toString());
            stmt.execute();
            JOptionPane.showMessageDialog(null, "Usuario cadastrado com sucesso!!");
            AtualizarTable();
            
            
            this.jUsuario.setText(null);
            this.jTipoUsuario.setSelectedItem("Selecione o Tipo");
            
        } catch (SQLException ex) {
            Logger.getLogger(ControleAcesso.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_btCadastrarMouseClicked

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
      this.jCodigo.setText(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
      this.jUsuario.setText(tabela.getValueAt(tabela.getSelectedRow(), 1).toString());
      situacao=tabela.getValueAt(tabela.getSelectedRow(),2).toString();
      this.jTipoUsuario.setSelectedItem(tabela.getValueAt(tabela.getSelectedRow(), 3).toString());
      
      if(situacao.equals("A")){
          this.rAtivo.setSelected(true);  
      }else if(situacao.equals("I")){
          this.rInativo.setSelected(true);
      }
      
      
      
        
        
        
    }//GEN-LAST:event_tabelaMouseClicked

    private void btResetarSenhaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btResetarSenhaMouseClicked
       try {
           Connection con = ConexaoMysql.conexaoBanco();
           String sql = "UPDATE login SET resetar_senha = 'S',senha=UPPER(MD5('Mudar123')) WHERE id_login=?;";
           PreparedStatement stmt = con.prepareStatement(sql);
           stmt.setString(1, this.jCodigo.getText());
           stmt.executeUpdate();
           stmt.close();
           con.close();
           JOptionPane.showMessageDialog(null,"Senha do "+this.jUsuario.getText()+" foi resetada para o padrão");
           AtualizarTable();
       } catch (SQLException ex) {
           Logger.getLogger(ControleAcesso.class.getName()).log(Level.SEVERE, null, ex);
       }
       
    }//GEN-LAST:event_btResetarSenhaMouseClicked

