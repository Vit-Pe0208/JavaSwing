    public void AtualizarTable(){
        try {
            Connection conexao = ConexaoMysql.conexaoBanco();
            String sql = "SELECT * FROM login ORDER BY id_login;";
            PreparedStatement stmt = conexao.prepareStatement(sql);
            DefaultTableModel tabela1 = (DefaultTableModel) tabela.getModel();
            tabela1.setNumRows(0);
            ResultSet rs = stmt.executeQuery();
            while(rs.next()){
                Object[] dados = {rs.getString("id_login"),rs.getString("usuario"),rs.getString("situacao"),rs.getString("tipo_usuario"),rs.getString("resetar_senha")};
                tabela1.addRow(dados);
            }
            stmt.close();
            rs.close();
            conexao.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(ControleAcesso.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

